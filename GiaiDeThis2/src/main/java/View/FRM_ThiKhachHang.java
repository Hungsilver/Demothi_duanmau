    public void loadTable(List<KhachHangCustom> list) {
        dtm.setRowCount(0);
        for (KhachHangCustom x : list) {
            dtm.addRow(new Object[]{
                x.getMa(),
                x.getHovaTen(),
                sdf.format(x.getNgaySinh()),
                x.getSdt(),
                x.getAddress()
            });
        }
        lblTotalKH.setText("Số lượng KH: " + KHService.getList().size());
    }

    public KhachHang getKhachHang() {
        Date ns = new Date(new java.util.Date(txtNgaySinh.getText()).getTime());
        return new KhachHang(null, txtMa.getText(), txtTen.getText(), txtTenDem.getText(), txtHo.getText(), ns, txtSDT.getText(), txtDC.getText(), txtTpho.getText(), txtQgia.getText(), null);
    }

    ;
public boolean checkTrungMa(String ma) {
        boolean check = true;
        for (KhachHangCustom x : KHService.getList()) {
            if (x.getMa().equalsIgnoreCase(ma)) {
                check = false;
            }
        }
        return check;
    }

