    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn Chắc chắn muốn thêm");
        if (confirm == 0) {
            KhachHang kh = getKhachHang();
            if (checkTrungMa(kh.getMa())) {
                if (KHService.save(kh)) {
                    JOptionPane.showMessageDialog(this, "Them thanh cong");
                    loadTable(KHService.getList());
                } else {
                    JOptionPane.showMessageDialog(this, "Them that bai");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Trùng mã rồi ô nội");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Đã hủy");
        }

    }//GEN-LAST:event_btnThemActionPerformed

    private void tblBangKHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBangKHMouseClicked
        // TODO add your handling code here:
        int rowcl = tblBangKH.getSelectedRow();
        idCL = KHService.getList().get(rowcl).getId();
        String fullName = tblBangKH.getModel().getValueAt(rowcl, 1).toString();
        String ho = fullName.split(" ")[0];
        String tenDem = fullName.split(" ")[1];
        String ten = fullName.split(" ")[2];

        String address = tblBangKH.getModel().getValueAt(rowcl, 4).toString();
        String dc = address.split(",")[0];
        String tp = address.split(",")[1];
        String qg = address.split(",")[2];

        txtMa.setText(tblBangKH.getModel().getValueAt(rowcl, 0).toString());
        txtHo.setText(ho);
        txtTenDem.setText(tenDem);
        txtTen.setText(ten);

        txtDC.setText(dc);
        txtTpho.setText(tp);
        txtQgia.setText(qg);
        txtNgaySinh.setText(tblBangKH.getModel().getValueAt(rowcl, 2).toString());
        txtSDT.setText(tblBangKH.getModel().getValueAt(rowcl, 3).toString());
    }//GEN-LAST:event_tblBangKHMouseClicked

